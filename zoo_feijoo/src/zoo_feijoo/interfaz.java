    private static java.util.List<java.lang.String> guardar(java.lang.String id, java.lang.String area, java.lang.String animalNombre, java.lang.String especie, java.lang.String habitad, java.lang.String cuidador, java.lang.String fechaIngreso) {
        zoo_feijoo.ZooWsConexion_Service service = new zoo_feijoo.ZooWsConexion_Service();
        zoo_feijoo.ZooWsConexion port = service.getZooWsConexionPort();
        return port.guardar(id, area, animalNombre, especie, habitad, cuidador, fechaIngreso);
    }

