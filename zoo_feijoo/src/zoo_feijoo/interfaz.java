    private static java.util.List<zoo_feijoo.StringArray> listar() {
        zoo_feijoo.ZooWsConexion_Service service = new zoo_feijoo.ZooWsConexion_Service();
        zoo_feijoo.ZooWsConexion port = service.getZooWsConexionPort();
        return port.listar();
    }

    private String[] convertirStringArray(StringArray stringArray) {
        List<String> lista = stringArray.getItem();
        return lista.toArray(new String[0]);
    }
    
    public void listarDatos(){
        try {
            DefaultTableModel tablaModelo = (DefaultTableModel) tabla_listar.getModel();
            tablaModelo.setColumnCount(0);
            tablaModelo.setRowCount(0);

            tablaModelo.addColumn("ID");
            tablaModelo.addColumn("Area");
            tablaModelo.addColumn("Animal");
            tablaModelo.addColumn("Especie");
            tablaModelo.addColumn("Habitad");
            tablaModelo.addColumn("Cuidador");
            tablaModelo.addColumn("Fecha de Ingreso");

            List<StringArray> datosZoo = listar();

            for (StringArray datos : datosZoo) {
                String[] fila = convertirStringArray(datos);
                tablaModelo.addRow(fila);
            }
        } catch (Exception ex) {
            System.out.println("Error al llenar la tabla... " + ex);
        }
    }
