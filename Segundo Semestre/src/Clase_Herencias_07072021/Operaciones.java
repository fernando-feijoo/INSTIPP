package Clase_Herencias_07072021;
public class Operaciones 
{
    public void Op_Sumar(double a, double b)
    {
        double c;
        c = (a + b);
        System.out.println("El resultado de la suma es: " + c);
    }
    public void Op_Restar(double a, double b)
    {
        double c;
        c = (a - b);
        System.out.println("El resultado de la resta es: " + c);
    }
    public void Op_Multiplicar(double a, double b)
    {
        double c;
        c = (a * b);
        System.out.println("El resultado de la multiplicacion es: " + c);
    }
    public void Op_Dividir(double a, double b)
    {
        double c;
        c = (a / b);
        System.out.println("El resultado de la division es: " + c);
    }
}
