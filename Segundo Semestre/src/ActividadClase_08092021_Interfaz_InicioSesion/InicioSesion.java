    private void btn_iniciosesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_iniciosesionActionPerformed
        // TODO add your handling code here:
        String user, pass;
        user = txp_usuario.getText();
        pass = txp_password.getText();
        if (user.equals("Admin") && pass.equals("12345")) 
        {
            Menu fm = new Menu();
            fm.show();
        }else if(!user.equals("Admin") && !pass.equals("12345"))
        {
            lbl_mensaje.setText("Usuario o contraseña incorrectos.");
        }
    }//GEN-LAST:event_btn_iniciosesionActionPerformed

