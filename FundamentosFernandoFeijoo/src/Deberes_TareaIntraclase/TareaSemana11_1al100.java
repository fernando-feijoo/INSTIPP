package Deberes_TareaIntraclase;
/* Realizar un programa que muestre por pantalla los numeros del 1 al 100 */
public class TareaSemana11_1al100 
{
    public static void main(String[] args)
    {
        int contador;
        contador = 1;
        do
        {
            System.out.println(contador);
            contador++;
        }while(contador <= 100);
    }   
}