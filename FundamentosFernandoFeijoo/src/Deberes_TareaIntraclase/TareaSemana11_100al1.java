package Deberes_TareaIntraclase;
/* Realizar un programa que muestre por pantalla los numeros del 100 al 1 */
public class TareaSemana11_100al1 
{
    public static void main(String[] args)
    {
        int contador;
        contador = 100;
        do
        {
            System.out.println(contador);
            contador--;
        }while(contador > 0);
    }   
}
